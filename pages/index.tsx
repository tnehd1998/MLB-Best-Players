import axios from "axios";
import type { NextPage } from "next";
import Head from "next/head";
import Image from "next/image";
import { useEffect, useState } from "react";

interface IPlayerData {
  name: string;
  team: string;
  years: number;
  SignedAge: number;
  averageValue: string;
  totalValue: string;
  period: string;
  position: string;
  playerImg: string;
}

const Home: NextPage = () => {
  const [data, setData] = useState<IPlayerData[]>([]);

  const getData = async () => {
    const response = await axios("http://localhost:3000/api/ranking");
    setData(response.data);
  };

  useEffect(() => {
    getData();
  }, []);

  return (
    <div>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className="flex flex-col justify-center items-center">
        <h1>MLB Top 100 FA Players</h1>
        <ul className="grid gap-4 grid-cols-4 grid-rows-3 text-center">
          {data?.map((player: IPlayerData, index: number) => (
            <div
              className="rounded-lg border-2 border-black hover:scale-110 cursor-pointer transition-all"
              key={index}
            >
              <Image
                className="rounded-t-lg"
                src={player.playerImg}
                width={200}
                height={300}
              />
              <h1>{player.name}</h1>
            </div>
          ))}
        </ul>
      </main>
    </div>
  );
};

export default Home;
